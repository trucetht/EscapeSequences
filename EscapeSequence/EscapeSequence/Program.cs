using System;

namespace EscapeSequence
{
    class Program
    {
        static void Main(string[] args)
        {
            // escape sequence for quotation marks
            Console.WriteLine("\"C#\" is really cool!");

            // escape sequence for single quotes
            Console.WriteLine("\'C#\' is really cool!");

            // escape sequence for a new line
            Console.WriteLine("C#\nis\nreally\ncool!");

            // escape sequence for an alert sound
            Console.WriteLine("\aC# is really cool!");
        }
    }
}
